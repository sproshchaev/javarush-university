package com.javarush.example;

public class Slide04_ForLoopExample {
    public static void main(String[] args) {
        System.out.println("=== Пример цикла for ===\n");

        // Простой пример: вывод чисел от 0 до 19
        for (int i = 0; i < 20; i++) {
            System.out.println(i);
        }

        System.out.println("\nЦикл завершён.");
    }
}
