public class Slide05_MethodParametersExample {

    public static void main(String[] args) {
        System.out.println("=== –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ ===");

        // üëá –í—ã–∑–æ–≤ –º–µ—Ç–æ–¥–∞ —Å —Ä–∞–∑–Ω—ã–º–∏ –∞—Ä–≥—É–º–µ–Ω—Ç–∞–º–∏
        printLines("–ü—Ä–∏–≤–µ—Ç!", 3);
        printLines("JavaRush", 2);
        printLines("–£—Ä–∞, —è –ø–æ–Ω—è–ª –ø–∞—Ä–∞–º–µ—Ç—Ä—ã!", 1);
    }

    /**
     * –ú–µ—Ç–æ–¥ printLines ‚Äî –≤—ã–≤–æ–¥–∏—Ç —É–∫–∞–∑–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –∑–∞–¥–∞–Ω–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–∞–∑.
     *
     * @param text  ‚Äî —Å—Ç—Ä–æ–∫–∞, –∫–æ—Ç–æ—Ä—É—é –Ω—É–∂–Ω–æ –≤—ã–≤–µ—Å—Ç–∏
     * @param count ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π
     */
    public static void printLines(String text, int count) {
        for (int i = 0; i < count; i++) {
            System.out.println(text);
        }
    }
}