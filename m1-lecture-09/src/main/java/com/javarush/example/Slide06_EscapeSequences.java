package com.javarush.example;

/**
 * Пример использования escape-последовательностей (экранирования символов)
 */
public class Slide06_EscapeSequences {

    public static void main(String[] args) {
        System.out.println("=== Экранирование символов ===\n");

        // \t — табуляция
        System.out.println("Имя:\tАлексей");
        System.out.println("Возраст:\t25\n");

        // \n — новая строка
        System.out.println("Первая строка\nВторая строка\nТретья строка\n");

        // \r — возврат каретки (редко используется, но работает)
        System.out.print("Привет\rМир!"); // В консоли может "перезаписать" начало строки
        System.out.println(); // Для чистоты вывода

        // \b — возврат на один символ назад (удаляет предыдущий символ)
        System.out.println("\nПривет\b\b\b\b\bПока"); // Удаляет 5 символов — "Привет" → "Пока"

        // \f — прогон страницы (в консоли обычно просто перенос строки)
        System.out.println("\fФорматирование страницы\n");

        // \' — одинарная кавычка
        System.out.println("Одинарная кавычка: \'");

        // \" — двойная кавычка (обязательно экранируем внутри строки!)
        System.out.println("Цитата: \"Java — это мощный язык!\"");

        // \\ — обратный слеш (если нужно вывести сам \)
        System.out.println("Обратный слеш: \\");

        // Пример реального использования — JSON или HTML внутри строки
        System.out.println("\n--- Реальный пример ---");
        String json = "{\"name\": \"Alice\", \"age\": 30}";
        System.out.println(json);
    }
}